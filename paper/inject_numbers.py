    macros["SOTAXGuardAcc"] = "0.7870"
    macros["DSBeaverTailsAcc"] = "0.6997"
    macros["SOTABeaverTailsAcc"] = "0.6815"
    macros["DSWildGuardMixAcc"] = "0.9686"
