    macros["SOTAWildGuardMixAcc"] = "0.9995"

    # Ablation
    macros["AblEuclideanLRAcc"] = "0.7605"
    macros["AblEuclideanLRAUC"] = "0.8350"
    macros["AblSingleHypAcc"] = "0.8120"
